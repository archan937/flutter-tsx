#!/usr/bin/env bun
/**
 * generate-types.ts
 *
 * Reads ref/derived/widgets.json + ref/derived/hooks.json and generates:
 *   src/generated/widget-interfaces.ts
 *   src/generated/widget-components.ts
 *   src/generated/widget-map.ts
 *   src/generated/slot-map.ts
 *   src/generated/feature-hooks.ts
 *   types/jsx.d.ts
 */

import { existsSync, mkdirSync, writeFileSync, readFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import type {
  FunctionDef,
  HookDef,
  PluginDef,
  PropDef,
  WidgetDef,
} from './define/api-types';
import {
  ANIMATED_TWIN,
  CURVE_NAMES,
  GESTURE_PROPS,
  LABEL_SUGAR_WIDGETS,
} from '../src/transpiler/widget-sugar';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT = join(__dirname, '..');

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function ensure(dir: string) {
  if (!existsSync(dir)) mkdirSync(dir, { recursive: true });
}

function write(filePath: string, content: string) {
  ensure(dirname(filePath));
  writeFileSync(filePath, content, 'utf-8');
  console.log(`  Written: ${filePath.replace(ROOT + '/', '')}`);
}

// The transpiler wraps a string assigned to a Widget-typed prop in `Text(...)`
// (see codegen `transform === 'widget'`), so a widget prop accepts a string
// convenience in addition to a FlutterElement. Array widget props (e.g.
// `actions: FlutterElement[]`) are not widened — you pass elements, not a string.
const tsTypeFor = (prop: PropDef): string =>
  prop.transform === 'widget' && prop.tsType === 'FlutterElement'
    ? 'string | FlutterElement'
    : prop.tsType;

const propToTsOptional = (prop: PropDef, forceOptional = false): string => {
  const optional = forceOptional || !prop.required ? '?' : '';
  return `  ${prop.tsxProp}${optional}: ${tsTypeFor(prop)};`;
};

function childrenPropLine(widget: WidgetDef): string {
  // Data-driven (no widget-name checks): a widget's children type comes from its
  // SDK-derived `childContent` classification.
  if (widget.childContent === 'text') {
    // Text-content widgets (Text, SelectableText): literal text and/or
    // interpolations. Mixed text + `{expr}` arrives as an array via jsxs(),
    // so allow `TextContent[]` too — fixes `<Text>Clicks: {count}</Text>`.
    return '  children?: TextContent | TextContent[];';
  }
  if (widget.childContent === 'widget') {
    // Always allow arrays: even "single-child" widgets (e.g. Scaffold.body) can
    // appear alongside slotted siblings (e.g. AppBar) in the same JSX block,
    // which makes TypeScript pass children as an array via jsxs().
    return '  children?: FlutterElement | FlutterElement[];';
  }
  return '';
}

